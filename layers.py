def test_number_of_layers():
    accs = []
    # define models with different number of conv layers
    models = [one_layer_model, two_layer_model, three_layer_model, four_layer_model, five_layer_model]
    for i, m in enumerate(models):
        model = m()
        model.fit(X_train, y_train, epochs=10, batch_size=64, validation_data=(X_test, y_test), verbose=2)
        _, acc = model.evaluate(X_test, y_test, verbose=0)
        accs.append(acc)
        print(f'Accuracy for {i + 1}-layer model= {acc * 100.0}')
    print(accs)
    plt.figure()
    plt.title('Accuracy for different number of layers')
    plt.xlabel('number of layers')
    plt.ylabel('accuracy')
    plt.plot([i + 1 for i in range(len(accs))], accs, 'o-', color='red')
    plt.show()


test_number_of_layers()